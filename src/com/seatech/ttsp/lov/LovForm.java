package com.seatech.ttsp.lov;

public class LovForm {
    public LovForm() {
        super();
    }
    private String ma;
    private String ten;
    private String loai;

    public void setMa(String ma) {
        this.ma = ma;
    }

    public String getMa() {
        return ma;
    }

    public void setTen(String ten) {
        this.ten = ten;
    }

    public String getTen() {
        return ten;
    }

    public void setLoai(String loai) {
        this.loai = loai;
    }

    public String getLoai() {
        return loai;
    }
}
